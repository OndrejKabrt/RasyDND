<h2>Staff</h2>
        <p>
            Neboli magická hůl. Praktický předmět pro mágy všeho věku. Mnohdy 
            vypadá jako dřevěná tyč s drahokamem nebo nějakým pestrobarevným 
            minerálem na samém vrcholu oné hole. Tyto kameny jsou standardně 
            používány mágy pro uschování many na pozdější použití. Kapacita, 
            kterou je krystal schopen uchovat, záleží na ryzosti onoho kamene. 
            Některé kameny jsou tak nečisté, že z nich může mana unikat a tím 
            pádem po nějakém čase energii ztratit úplně. Hůl může být uzpůsobena 
            tak, že mág skrze ni dokáže zprostředkovávat specifická kouzla, ale 
            tyto hole mnohdy dosahují vyšších cenových tříd.
        </p>
        <p>
            Nevýhodou pro zloděje a výhodou pro vás je, že kouzlo schované v 
            krystalu se nedá zjistit bezpečnou a lehkou cestou. Výslovně se 
            nedoporuču je tato kouzla v krystalu zkoušet.
        </p>
