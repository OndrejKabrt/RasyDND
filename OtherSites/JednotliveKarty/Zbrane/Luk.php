<h2>Luk</h2>
        <p>
            Luk je v této hře speciální kousek vybavení. Jako předmět uděluje 
            poškození pouze v případě, že s ním hráčská postava mlátí jinou 
            postavu. K udělení pořádného poškození musí hráč použít šíp, který 
            s sebou nosí. Od šípu se pak odvíjí poškození, které hráč uděluje. 
            Samozřejmě nejlepší luky jsou dílem výroby dryád z Brokilonu, k těm 
            se mnohdy nedostanou blízko a živí najednou, tudíž na dalším místě 
            jsou luky ze specializovaných výroben.
        </p>
