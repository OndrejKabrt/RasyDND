<h2>Meč</h2>
        <p>
            Zbraň, která byla v historii jednou z nejpoužívanějších, jak v období 
            válek, tak v obdobích míru. Existují nespočet druhů mečů, které byly 
            kdy vykovány. Meče mají určitou délku, váhu, ostrost a další důležité 
            parametry. V Zaklínačském D&D se u mečů však moc věcí nemění; standardní 
            je počet hodů kostkou a také kolika stěnou kostkou. To se mnohdy různí 
            podle rasy tvůrce, ostrosti a schopnosti hráče s mečem zacházet.
        </p>
        <p>
            Nejlepší meče, co na světě najdete, jsou meče gnomské výroby. Ty 
            nejostřejší a nejdražší meče, které na světě prolévaly krev.
        </p>
